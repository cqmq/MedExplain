export function isLocale(value: string | null | undefined): value is Locale {
  return value === "en" || value === "tr" || value === "ar";
}

export function localeFromLanguage(language?: string | null): Locale {
  const normalized = language?.toLowerCase() ?? "";
  if (normalized.includes("arabic") || normalized.includes("عربي")) return "ar";
  if (normalized.includes("turkish") || normalized.includes("türk")) return "tr";
  return "en";
}

